# Example 1: Using triple quotes

my_string = '''This only way to
learn to program is
by writing code.'''

print(my_string)

# Output
# This only way to
# learn to program is
# by writing code.

# Example 2: Using parentheses and a single/double quotes

my_string = ("The only way to \n"
             "learn to program is \n"
             "by writing code.")

print(my_string)

# Output
# This only way to
# learn to program is
# by writing code.
